<p><strong>Description</strong></p>
<p>This extension adds a functionality for inserting and displaying math equations (TeX/LaTeX) and figures (PSPicture).</p>
<p><strong>Structure</strong></p>
<ul>
	<li>Component - includes helper files for the editor button plugin</li>
	<li>Button plugin - adds a button to the editor for inserting math equations and figures</li>
	<li>Content plugin - provides math support for articles</li>
	<li>System plugin - provides math support for front-end web pages</li>
</ul>
